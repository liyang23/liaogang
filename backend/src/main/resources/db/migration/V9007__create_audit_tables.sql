-- U9 审计日志按月分区表 (T308)
-- 12 月保留 (NFR-15) + PK 复合 (id, created_at) 支持 PARTITION BY RANGE
-- 与 V9006__create_snapshot_tables.sql 同 wave

-- 注意: PK 复合 (id, created_at) 才能按月分区. 字符串 AUDIT ID (id) 单独无法做 partition key.
-- 12 月保留的冷存储归档策略属于运维层 (基础架构团队另行规划).

CREATE TABLE IF NOT EXISTS `km_audit_log` (
    `id` VARCHAR(32) NOT NULL COMMENT 'AUDIT-{YYYYMMDD}-{NNNNNN} 格式, 按天独立自增',
    `action` VARCHAR(32) NOT NULL COMMENT 'KO_CREATE / KO_UPDATE / KO_DELETE / KO_VERSION_PUBLISH / KO_REVIEW / CONFLICT_RESOLVE / USER_CONFLICT_ARBITRATE / USER_ROLE_LOGIN / LLM_SUGGEST_REQUESTED / SNP_STALE_DETECTED / DOC_PREVIEW_ACCESSED 等',
    `user_id` VARCHAR(64) NOT NULL COMMENT '操作人 sub (来自 JWT)',
    `target_ko` VARCHAR(32) NULL COMMENT '操作目标 KO ID (可选)',
    `detail` TEXT NULL COMMENT '变更详情 (JSON 字符串)',
    `reason` VARCHAR(255) NULL COMMENT '操作理由 (可选)',
    `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'OQ-5 简化: 无 reverted_at 字段',
    PRIMARY KEY (`id`, `created_at`),
    KEY `idx_user` (`user_id`, `created_at`),
    KEY `idx_target` (`target_ko`, `created_at`),
    KEY `idx_action_time` (`action`, `created_at`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4
PARTITION BY RANGE (TO_DAYS(`created_at`)) (
    PARTITION p202507 VALUES LESS THAN (TO_DAYS('2025-08-01')),
    PARTITION p202508 VALUES LESS THAN (TO_DAYS('2025-09-01')),
    PARTITION p202509 VALUES LESS THAN (TO_DAYS('2025-10-01')),
    PARTITION p202510 VALUES LESS THAN (TO_DAYS('2025-11-01')),
    PARTITION p202511 VALUES LESS THAN (TO_DAYS('2025-12-01')),
    PARTITION p202512 VALUES LESS THAN (TO_DAYS('2026-01-01')),
    PARTITION p202601 VALUES LESS THAN (TO_DAYS('2026-02-01')),
    PARTITION p202602 VALUES LESS THAN (TO_DAYS('2026-03-01')),
    PARTITION p202603 VALUES LESS THAN (TO_DAYS('2026-04-01')),
    PARTITION p202604 VALUES LESS THAN (TO_DAYS('2026-05-01')),
    PARTITION p202605 VALUES LESS THAN (TO_DAYS('2026-06-01')),
    PARTITION p202606 VALUES LESS THAN (TO_DAYS('2026-07-01')),
    PARTITION p202607 VALUES LESS THAN (TO_DAYS('2026-08-01')),
    PARTITION p202608 VALUES LESS THAN (TO_DAYS('2026-09-01')),
    PARTITION p_future VALUES LESS THAN MAXVALUE
);
